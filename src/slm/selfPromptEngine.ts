/**
 * L2 Self-Prompt Reasoning Engine
 *
 * Four operating modes (in order of preference):
 *
 * 1. Ornith SLM (vLLM) — highest quality reasoning with <think> trace.
 *    Requires: ORNITH_BASE_URL and ORNITH_MODEL env vars set.
 *    Ornith uses Qwen3-style reasoning: returns both a reasoning trace and
 *    a final answer. The reasoning trace is stored in ReasoningResponse.reasoningTrace.
 *
 * 2. Fine-tuned Ollama SLM — fastest, most accurate, runs locally.
 *    Requires: `ollama create codebase-slm -f ./Modelfile` after training.
 *
 * 3. Base Ollama model with in-context few-shot examples — no training needed.
 *    Requires: `ollama pull qwen2.5-coder:1.5b` and a few_shot_examples.json
 *    generated by `python finetunePipeline.py --in-context`.
 *    Set OLLAMA_MODEL=codebase-slm-base and FEW_SHOT_PATH=./models/few_shot_examples.json
 *
 * 4. OpenAI-compatible API fallback — works everywhere, costs per token.
 *    Automatic when Ollama is unavailable.
 *
 * Design constraints:
 * - Max 200 lines per file
 * - Max 20 lines per function
 */

import { readFileSync, existsSync } from 'fs';

export type ReasoningMode = 'diagnose' | 'repair' | 'explain' | 'dream' | 'relate';

export interface ReasoningRequest {
  mode: ReasoningMode;
  context: string;
  nodeId?: string;
  failureDescription?: string;
}

export interface ReasoningResponse {
  mode: ReasoningMode;
  output: string;
  model: string;
  durationMs: number;
  tokenCount?: number;
  /** Ornith <think> reasoning trace, when ORNITH_BASE_URL is set. */
  reasoningTrace?: string;
}

export interface SelfPromptEngineConfig {
  ollamaUrl?: string;
  ollamaModel?: string;
  fallbackToOpenAI?: boolean;
  timeoutMs?: number;
  fewShotPath?: string;       // Path to few_shot_examples.json from --in-context mode
  /** Ornith vLLM base URL. When set, Ornith is used as the primary inference backend. */
  ornithBaseUrl?: string;
  /** Ornith model name (default: ORNITH_MODEL env or 'ornith-1.0-9b'). */
  ornithModel?: string;
}

const DEFAULT_CONFIG: Required<SelfPromptEngineConfig> = {
  ollamaUrl: process.env['OLLAMA_URL'] ?? 'http://localhost:11434',
  ollamaModel: process.env['OLLAMA_MODEL'] ?? 'codebase-slm',
  fallbackToOpenAI: true,
  timeoutMs: 30_000,
  fewShotPath: process.env['FEW_SHOT_PATH'] ?? '',
  ornithBaseUrl: process.env['ORNITH_BASE_URL'] ?? '',
  ornithModel: process.env['ORNITH_MODEL'] ?? 'ornith-1.0-9b',
};

export class SelfPromptEngine {
  private readonly config: Required<SelfPromptEngineConfig>;
  private fewShotPrefix: string = '';

  constructor(config: SelfPromptEngineConfig = {}) {
    this.config = { ...DEFAULT_CONFIG, ...config };
    this.fewShotPrefix = this.loadFewShotExamples();
  }

  /**
   * Load few-shot examples from disk if a path is configured.
   * Returns an empty string if no path is set or file does not exist.
   */
  private loadFewShotExamples(): string {
    const path = this.config.fewShotPath;
    if (!path || !existsSync(path)) return '';
    try {
      const examples = JSON.parse(readFileSync(path, 'utf-8')) as Array<{
        instruction: string;
        input?: string;
        output: string;
      }>;
      const lines = ['### Codebase Examples (use these to understand the patterns):\n'];
      for (const ex of examples.slice(0, 6)) {
        lines.push(`Q: ${ex.instruction}`);
        if (ex.input?.trim()) lines.push(`Context: ${ex.input.slice(0, 200)}`);
        lines.push(`A: ${ex.output.slice(0, 300)}\n`);
      }
      return lines.join('\n');
    } catch {
      return '';
    }
  }

  /**
   * Send a reasoning request to the SLM.
   * Priority: Ornith (if ORNITH_BASE_URL set) → Ollama → OpenAI fallback.
   */
  public async reason(req: ReasoningRequest): Promise<ReasoningResponse> {
    const prompt = this.buildPrompt(req);
    const start = Date.now();
    if (this.config.ornithBaseUrl) return this.callOrnith(prompt, req.mode, start);
    const ollamaAvailable = await this.checkOllama();
    if (ollamaAvailable) return this.callOllama(prompt, req.mode, start);
    if (this.config.fallbackToOpenAI) return this.callOpenAI(prompt, req.mode, start);
    throw new Error('Ollama unavailable and OpenAI fallback is disabled.');
  }

  /**
   * Call the Ornith vLLM endpoint (OpenAI-compatible /v1/chat/completions).
   * Extracts the <think>...</think> reasoning trace from the response when present.
   */
  private async callOrnith(
    prompt: string, mode: ReasoningMode, start: number
  ): Promise<ReasoningResponse> {
    const res = await fetch(`${this.config.ornithBaseUrl}/v1/chat/completions`, {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify({
        model: this.config.ornithModel,
        messages: [{ role: 'user', content: prompt }],
        temperature: 0.2,
        max_tokens: 2048,
      }),
    });
    const data = await res.json() as {
      choices: Array<{ message: { content: string; reasoning_content?: string } }>;
      usage?: { total_tokens: number };
    };
    const raw = data.choices[0]?.message.content ?? '';
    // Prefer server-side reasoning_content (vLLM --reasoning-parser qwen3)
    const serverReasoning = data.choices[0]?.message.reasoning_content;
    const { reasoning, answer } = serverReasoning
      ? { reasoning: serverReasoning, answer: raw }
      : this.extractReasoningAndAnswer(raw);
    return {
      mode,
      output: answer,
      model: this.config.ornithModel,
      durationMs: Date.now() - start,
      tokenCount: data.usage?.total_tokens,
      reasoningTrace: reasoning || undefined,
    };
  }

  /**
   * Extract <think>...</think> reasoning trace and final answer from raw output.
   * Returns { reasoning: string, answer: string }.
   */
  private extractReasoningAndAnswer(raw: string): { reasoning: string; answer: string } {
    const thinkMatch = raw.match(/<think>([\s\S]*?)<\/think>/i);
    if (!thinkMatch) return { reasoning: '', answer: raw.trim() };
    const reasoning = thinkMatch[1]?.trim() ?? '';
    const answer = raw.replace(/<think>[\s\S]*?<\/think>/i, '').trim();
    return { reasoning, answer };
  }

  /**
   * Build the structured prompt. Prepends few-shot examples when available.
   */
  private buildPrompt(req: ReasoningRequest): string {
    const modeInstructions: Record<ReasoningMode, string> = {
      diagnose: 'Diagnose the following failure. Output DIAGNOSIS, LOCATION, FIX, TEST, RISK.',
      repair: 'Generate a TypeScript fix for the following issue. Follow all project conventions.',
      explain: 'Explain what the following code does in plain language.',
      dream: 'Generate a hypothesis about what could be improved. Be creative but grounded.',
      relate: 'Identify all dependencies and relationships in the following code.'
    };
    const instruction = modeInstructions[req.mode];
    const context = req.failureDescription
      ? `${req.failureDescription}\n\n${req.context}`
      : req.context;
    const prefix = this.fewShotPrefix ? `${this.fewShotPrefix}\n---\n\n` : '';
    return `${prefix}### Instruction:\n${instruction}\n\n### Input:\n${context}\n\n### Response:\n`;
  }

  /**
   * Check if the Ollama instance is running and the model is loaded.
   */
  private async checkOllama(): Promise<boolean> {
    try {
      const controller = new AbortController();
      const timeout = setTimeout(() => controller.abort(), 2000);
      const res = await fetch(`${this.config.ollamaUrl}/api/tags`, {
        signal: controller.signal
      });
      clearTimeout(timeout);
      if (!res.ok) return false;
      const data = await res.json() as { models?: Array<{ name: string }> };
      return data.models?.some(m => m.name.includes(this.config.ollamaModel)) ?? false;
    } catch {
      return false;
    }
  }

  /**
   * Call the local Ollama instance with the given prompt.
   */
  private async callOllama(
    prompt: string, mode: ReasoningMode, start: number
  ): Promise<ReasoningResponse> {
    const res = await fetch(`${this.config.ollamaUrl}/api/generate`, {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify({
        model: this.config.ollamaModel,
        prompt,
        stream: false,
        options: { temperature: 0.2, num_predict: 1024 }
      })
    });
    const data = await res.json() as { response: string; eval_count?: number };
    return {
      mode, output: data.response, model: this.config.ollamaModel,
      durationMs: Date.now() - start, tokenCount: data.eval_count
    };
  }

  /**
   * Fall back to the OpenAI-compatible API when Ollama is unavailable.
   */
  private async callOpenAI(
    prompt: string, mode: ReasoningMode, start: number
  ): Promise<ReasoningResponse> {
    const baseUrl = process.env['OPENAI_API_BASE'] ?? 'https://api.openai.com/v1';
    const apiKey = process.env['OPENAI_API_KEY'] ?? '';
    const res = await fetch(`${baseUrl}/chat/completions`, {
      method: 'POST',
      headers: { 'Content-Type': 'application/json', 'Authorization': `Bearer ${apiKey}` },
      body: JSON.stringify({
        model: 'gpt-4o-mini',
        messages: [{ role: 'user', content: prompt }],
        temperature: 0.2, max_tokens: 1024
      })
    });
    const data = await res.json() as {
      choices: Array<{ message: { content: string } }>;
      usage?: { total_tokens: number };
    };
    return {
      mode, output: data.choices[0]?.message.content ?? '',
      model: 'openai-fallback', durationMs: Date.now() - start,
      tokenCount: data.usage?.total_tokens
    };
  }
}
